function plot_trajectories(XY_all, xy_max)
% PLOT_TRAJECTORIES Plot agent trajectories over time.
%
%   plot_trajectories(XY_all, xy_max)
%
%   Inputs:
%       - XY_all: Array containing agent positions over time
%                 (n_agents x n_timesteps x 2).
%       - xy_max: Maximum XY coordinate for the environment.
%
%   Details:
%       This function plots the trajectories of agents over time using the
%       provided agent positions stored in 'XY_all'. It also adds markers at
%       the initial (black circles) and final (red circles) positions of each
%       agent. The plot is displayed within the specified XY coordinate limits
%       defined by 'xy_max'. The plot is made square, and labels for the X and
%       Y axes are added.
%
%   Inputs:
%       - XY_all: Array containing agent positions over time (n_agents x n_timesteps x 2).
%       - xy_max: Maximum XY coordinate for the environment.
%
%   See also: SIMULATE_AGENTS, INITIALIZE_AGENTS, INITIALIZE_ENVIRONMENT,
%             PERCEPTION, POLICY_DIFFUSION, POLICY_LEVY, POLICY_SEEK_ODOR, PHYSICS.
%
%   Author: Emily Mackevicius
%   Date: October 13, 2023
%   docstring generated by LLM
plot(XY_all(:,:,1)', XY_all(:,:,2)')
hold on
scatter(XY_all(:,1,1)', XY_all(:,1,2)', 'ko', 'markerfacecolor', 'flat')
scatter(XY_all(:,end,1)', XY_all(:,end,2)', 'ro', 'markerfacecolor', 'flat')
xlim([0 xy_max])
ylim([0 xy_max])
axis square
xlabel('x')
ylabel('y')
end